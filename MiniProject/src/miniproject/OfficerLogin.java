    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        // TODO add your handling code here:
        String fname = firstNameTextField.getText();
        String lname = lastNameTextField.getText();        
        String email = emailLoginTextField.getText();
        String password = loginPasswordField.getText();
        int phone = Integer.parseInt(phoneTextField.getText());
        String query = "insert into officer values('"+fname+"','"+lname+"','"+email+"','"+password+"',"+phone+"')";
        try{
            Connection con = (Connection)DriverManager.getConnection(dburl, uid,pwd);
            Statement stmt = (Statement)con.createStatement();
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Officer registered Successfully");
            new OfficerList(email).setVisible(true);
            this.setVisible(false);
        }catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_registerButtonActionPerformed

