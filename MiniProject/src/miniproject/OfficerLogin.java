    private void loginPlayerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginPlayerButtonActionPerformed
        // TODO add your handling code here:
        String email = emailLoginTextField.getText();
        String password = loginPasswordField.getText();
        String query = "select pass from officer where email = '"+email+"'";
        try{
            Connection con = (Connection)DriverManager.getConnection(dburl, uid,pwd);
            Statement stmt = (Statement)con.createStatement();
            ResultSet rset = stmt.executeQuery(query);
            while(rset.next()){
                if(password.equals(rset.getString("pass"))){
                    new OfficerList(email).setVisible(true);
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Incorrect email or password");
                }
            }
        }catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_loginPlayerButtonActionPerformed

